     */
    public function update(Request $request, $id)
    {
        $konfir = Karyawan::find($id);

        if ($konfir->cek == 0) {
            $konfir->cek = 1;
            Verify::create([
                'karyawan_id' => $id,
                'name' => Auth::user()->name
            ]);
        }
        $konfir->save();

        return redirect()->route('karyawan.index');
